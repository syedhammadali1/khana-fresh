     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->merge(['locale_id' => ($request->locale_id) ? $request->locale_id : 1]);
        Faq::updateOrCreate(['id' => $request->page_id], $request->all());

        return redirect()->route('faqs.index',['locale_id'=>$request->locale_id])->with('class', 'success')->with('message', 'FAQs add successfully.');
    }

